                <div class="advisor-content">
                  The Kishore Vaigyanik Protsahan Yojana (KVPY) is a National Program of Fellowship in Basic Sciences, initiated and funded by the Department of Science and Technology, Government of India, to attract exceptional and highly motivated students for pursuing basic science courses and research careers in science.
                  <div class='show-more-text'>
                  <br><br><strong>Official Notification Timeline:</strong>

                  <br>The advertisement for the KVPY Scholarship exam appears in all the national dailies usually on Technology Day (May 11) and the Second Sunday of July every year.

                  <br><br><strong>Selection Criteria:</strong>
                  <br>
                  Based on the performance in the aptitude test, short-listed students are called for an interview which is the final stage of the selection procedure. For receiving a fellowship, both aptitude test and interview marks are considered. Generous fellowship and contingency grant are provided to the selected KVPY Fellows up to the pre Ph.D. level or 5 years whichever is earlier.

<br><br><strong>Eligibility:</strong>
 <br>Students presently pursuing studies in Class XI, XII (of the regular 10 + 2 scheme of study) or pursuing first year of study in a basic science stream of a three year Bachelor program or a five year Integrated Masters Program are eligible to apply. Read on for more specific details:
 
<br><br>(a) Stream SA: Students should be enrolled in XI Standard (Science Subjects) during the given academic year and should have secured a minimum of 80% (70% for SC/ ST/ PWD) marks in aggregate in MATHEMATICS and SCIENCE subjects (Physics/ Chemistry/ Biology) in the X Standard Board Examination. If the students have obtained a letter grade in the qualified examination, they are requested to convert it into an appropriate % marks and fill-up the application form.
 
<br><br>(b) Stream SB: Students enrolled in the 1st Year of an undergraduate program in sciences (B.Sc./B.S./ B.Stat./B.Math./Int. M.Sc./M.S.) during the given academic year and having secured a minimum of 60% (50% for SC/ST/PWD) marks in aggregate in Science subjects (Physics/ Chemistry/ Biology) in the XII/ (+2) Standard Board Examination. In the 1st year final examination B.Sc./B.S./B.Math./B.Stat./Int. M.Sc./M.S. they must secure 60% (50% for SC/ST/PWD) marks before taking up the fellowship, if awarded.
 
<br><br>(c) Stream SX: Students enrolled in XII standard / (+2) during the given academic year and aspiring to join an undergraduate program in Basic Sciences (B.Sc./B.S./B.Stat./B.Math./Int. M.Sc./M.S.) for the consecutive academic session provided they have secured a minimum of 80% (70% for SC/ST/PWD) marks in aggregate in MATHEMATICS and SCIENCE subjects (Physics/ Chemistry/ Biology) in the X Standard Board Examination. They must secure 60% (50% for SC/ST/PWD) marks in aggregate in MATHEMATICS and SCIENCE subjects (Physics/ Chemistry/ Biology) in the XII standard Board Examination before taking up the fellowship, if awarded.
