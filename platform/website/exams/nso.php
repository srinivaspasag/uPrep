                <div class="advisor-content">
                  The National Science Olympiad (NSO) is a yearly competitive exam conducted for school students. Organized by the Science Olympiad Foundation (SOF), this exam aims at cultivating scientific reasoning and logical ability among the students through a rigorous test. <div class='show-more-text'>The Science Olympiad Foundation primarily instills scientific curiosity and vigour in young students through competitive exams.

                  <br><br><strong>Eligibility Criteria:</strong>

                  <br>Any student from class 1 to class 12 of boards CBSE / SSC / ICSE can appear for the exam.

                  <br><br><strong>Selection Process:</strong>
                  <br>
                  There are two levels:
<br>Level 1: Any one can appear from the school.
<br>Level 2: The top 5% of students (class-wise) who appear for the first level exam. The top 25 rank holders state-wise. Class toppers from each participating school; where at least 10 students participate and score a minimum of 50%.

