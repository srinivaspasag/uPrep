                <div class="advisor-content">
                  The Joint Entrance Examination (JEE) is an All-India Common Entrance Exam for getting admissions into engineering colleges across India. JEE Main will be conducted twice a year in the month of January and April respectively by the National Testing Agency (NTA). <div class='show-more-text'>It is conducted in two phases: JEE Main and JEE Advanced. If the student clears the given cut-off for JEE Main, then the student qualifies for JEE Advanced. With a JEE Main score one can apply to institutions like NITs, IIITs and other Central or State Funded Institutes. Whereas, IITs and ISM, Dhanbad consider the results of JEE Advanced for admissions. From 2020 the examination will be held only in the computer based mode.

                  <br><br><strong>Eligibility Criteria:</strong>

                  <br><strong>Age:</strong>
<br>General category candidates must have been born on or after 1st October, 1994.
SC, ST and PWD candidates must have been born on or after 1st October, 1989.

<br><br><strong>Subjects:</strong>
<br>The student must have studied at least 5 subjects in 10+2. Physics and Mathematics are compulsory subjects. Chemistry, Biotechnology, Biology or Technical vocational are the other subjects to select from.

<br><br><strong>Score in 12th standard or equivalent exam:</strong>
<br>General category candidates require a minimum score of least 75% or should be in the top 20 percentile in their respective 12th board or equivalent examination. For an SC or ST candidate, the minimum score is 65%.

<br><br><strong>Year of passing 12th standard or equivalent exam:</strong>
<br>The student must have passed Class 12th or any equivalent qualifying examination in the years 2017, 2019 or 2020.

<br><br><strong>Aadhar Card requirement:</strong>
<br>One needs a registered Aadhaar Card Number to be eligible to apply for JEE Main.

<br><br><strong>Number of attempts:</strong>
<br>One can attempt JEE Main twice in a year - once in January and once in April. The best score will be considered for qualifying into JEE Advanced and admissions to colleges. The student can attempt JEE Advanced only for 2 consecutive years.

