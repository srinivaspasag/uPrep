var printIndexString = true;

function printUserAttempts(attempts,userId,score,printIndexString,qScoreArr){
    var quesAttemptString = "";
    var indexString = "";
    for(i=0;i<attempts.length;i++){
        indexString += (i+1)+","+"qScore"+",";
        quesAttemptString += attempts[i]+","+qScoreArr[i]+","; 
    }
    if(printIndexString){
        print("MemberId"+","+"Name"+","+indexString+"Score");
    }
    db.orgmembers.find({userId:userId}).forEach(function(user){
        print(user.memberId+","+"\""+user.firstName+" "+user.lastName+"\""+","+quesAttemptString+score);
    });
}


for(k=0;k<users.length;k++){
    var score = 0.0;
    db.userquestionattempts.find({"parentEntity.id":testId,userId:users[k],isFinalized:true}).forEach(function(uqa){
        var index = questions.indexOf(uqa.qId);
        
        qScore[index] = parseFloat(uqa.score);
        score += qScore[index];

        
        if(index != -1){
            if(uqa.type != "NUMERIC"){
                if(uqa.answerGiven.length >1){
                    var alphaIndex = "";
                    for(j=0;j<uqa.answerGiven.length;j++){
                        alphaIndex += alphabets[uqa.answerGiven[j]]+" ";
                        userAttempt[index] = alphaIndex;
                    }
                }
                else{
                    alphaIndex = alphabets[uqa.answerGiven[0]];
                    userAttempt[index] = alphaIndex;
                }
            }
            else{
                userAttempt[index] = uqa.answerGiven[0];
            }
        }
    });
    printUserAttempts(userAttempt,users[k],score,printIndexString,qScore);
    printIndexString = false;
    for(i=0;i<questions.length;i++){
        userAttempt[i] = "N/A";
    }
    for(i=0;i<questions.length;i++){
        qScore[i] = 0;
    }
}