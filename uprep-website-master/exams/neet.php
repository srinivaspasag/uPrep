<br><br>As a SC-PH, ST-PH or OBC-PH candidate, the student requires a minimum aggregate score of 40%.

<br><br>As a PH, PwD or Gen-PH candidate, the student requires a minimum aggregate score of 45%.

<br><br><strong>Number of attempts:</strong>
<br>One can attempt the exam as many times as one wants till the age of 25.


                  <br><br><strong>Exam pattern:</strong>
                  <br>
                  The 2020 NEET examination will be conducted through offline mode (paper based). It will contain only one question paper which comprises 180 questions. Questions will be objective type. Each question will contain four optional answers. The question papers will be composed of four sections i.e. Physics, Chemistry, Botany and Zoology.


