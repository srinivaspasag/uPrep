                <div class="advisor-content">
                  National Talent Search Exam (NTSE) is a scholarship programme conducted by NCERT, as a part of their National Talent Search Scheme launched way back in 1963 with an objective to identify talented students and encourage them to pursue higher education. <div class='show-more-text'>The scheme provides financial assistance to meritorious students in the form of monthly scholarship. Students who are awarded the scholarship receive financial assistance up to PhD level in Social Sciences, Basic Sciences and Commerce. The scheme has undergone many changes in the pattern, structure and various other aspects over the years in response to the changing needs of the children and the conditions of the society. Though, it was initially aimed at motivating deserving students to go for higher studies, with the growing importance to higher education in the present day society, the objective of the programme is more focussed on recognising students with extremely high intellectual abilities and academic excellence. That means, in addition to providing scholarships to students throughout their future academic career, a rewarding performance in NTSE has now become a very valuable addition to the credentials of a child.

                  <br><br><strong>No of Scholarships:</strong>

                  <br>As per the present rules of the NTSE scheme, 1000 scholarships are awarded each year.

                  <br><br><strong>Details of the Scholarships:</strong>
                  <br>
                  (a) A scholarship of Rs.1250/- per month for class-XI to XII.
<br>(b) A scholarship of Rs.2000/- per month for Undergraduates and Postgraduates.
<br>(c) The amount of Scholarship for PhD will be fixed in accordance with the UGC norms.

<br><br><strong>Eligibility:</strong>
 <br>All students studying in recognised schools of class X are eligible to appear for the Stage-I NTS Examination. Student from any board can appear for the examination. The scheme is open only to students of Indian nationality and for those Indian students studying abroad in class 10 can appear directly for Stage-II NTS Examination.
 
<br><br><strong>Mode of selection:</strong>
 <br>The selection process consists of two levels, the preliminary exam (Stage - I) is conducted by the respective state/UT boards generally in the month of November. The students who qualify in Stage - I, appear for Stage - II examination conducted by NTSE generally in the month of May.

