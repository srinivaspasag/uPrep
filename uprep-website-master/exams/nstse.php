<br><br><strong>Selection process:</strong>
 <br>The NSTSE exam is conducted at two levels. Level 1 is conducted at the schools and at some selected test centers. The top 500 participants from Class 2 to 12 across the nation, along with the top 20 from each class are selected for Level 2. Level 2 is conducted at the nearest State Headquarters. On the basis of this test, the final NSTSE results are declared.
 
