                  The International Mathematical Olympiad (IMO) is the world championship mathematics competition for high school students and is held annually in a different country. The first IMO was held in 1959 in Romania, with 7 countries participating. It has gradually expanded to over 100 countries from 5 continents. The IMO board ensures that the competition takes place each year and that each host country observes the regulations and traditions of the IMO
                  <div class='show-more-text'>
                  <br><br><strong>Eligibility Criteria:</strong>

                  <br>Students of classes 1 to 12 are eligible to appear for the 1st level Olympiads. There is no other eligibility criterion like minimum marks.

                  <br><br><strong>Selection process:</strong>
                  <br>
                  <strong>Level 1:</strong>
<br>As mentioned, anyone from classes 1 to 12 can appear for the Level 1 exam, post which Level 2 exam takes place.
<br><strong>Level 2:</strong>
<br>Top 5% of students’, class wise, who appear for the 1st level exam.
Zone wise top 25 rank holders, class wise. Class toppers from each participating school, where at least 10 students from a class appear in the exam, and score 50% qualifying marks. In case, the registration is received section wise, students with highest marks, irrespective of section will qualify for the 2nd level. For section wise registrations, winner(s) of gold medal may not necessarily qualify for the second level. Students from classes 1 and 2 are not required to appear for the 2nd level exam and are ranked based on their performance in the first level exam.


<br><br><strong>Exam pattern:</strong>
 <br><strong>For classes 1st to 4th:</strong>

<br>The student has to answer 35 questions in 60 minutes. There will be a separate question paper for each class.

<br>Question paper will have four sections:
<br>Section I – Logical Reasoning (10 questions)
<br>Section II – Mathematical Reasoning (10 questions)
<br>Section III – Everyday Mathematics (10 questions)
<br>Section IV – Achievers Section (5 questions)

<br><br><strong>For classes 5th to 12th:</strong>

<br>The student has to answer 50 questions in 60 minutes. There will be a separate question paper for each class.

<br><br>Question paper will have four sections:
<br>Section I - Logical Reasoning (15 questions)
<br>Section II - Mathematical Reasoning (20 questions)
<br>Section III - Everyday Mathematics (10 questions)
<br>Section IV - Achievers Section (5 questions)

