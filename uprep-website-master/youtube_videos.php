  <script type="text/javascript">
    var translatedSlideHash;
    jQuery(document).ready(function($) {
                "use strict";
                //  TESTIMONIALS CAROUSEL HOOK
                $('#youtube-video-testimonials').owlCarousel({
                    // loop: true,
                    // center: true,
                    // items: 3,
                    margin: 10,
                    // autoplay: true,
                    dots:true,
                    nav:true,
                    // URLhashListener:true,
                    // autoplayHoverPause:true,
                    // startPosition: 'URLHash',
                        navText: [
                      '<i class="fa fa-angle-left" aria-hidden="true"></i>',
                      '<i class="fa fa-angle-right" aria-hidden="true"></i>'
                  ],
                    // autoplayTimeout: 8500,
                    // smartSpeed: 450,
                    responsive: {
                      0: {
                        items: 1
                      },
                      768: {
                        items: 2,
                        slideBy:2,
                        mouseDrag:false
                      }
                      ,
                      1170: {
                        items: 2,
                        slideBy:2,
                        mouseDrag:false
                      }
                    }
                });
                $("#youtube-video-testimonials").on("translated.owl.carousel",function(){
                  translatedSlideHash = "#" + $(this).find(".owl-item.active .item").attr("data-url-hash");
                  $(".subject-block").each(function(){
                    if(translatedSlideHash.includes($(this).attr("data-href"))){
                      $(".subject-block").removeClass("active");
                      $(this).addClass("active");
                    }
                  });
                })
            });

    $(".youtube-img-thumbnail-holder").off("click")
                                  .on("click",loadVideoInModel)

    $(".subject-block").off("click")
                        .on("click",makeSubjectActive)


    function makeSubjectActive(){
      var alreadyHashedCount = 1;
      if(translatedSlideHash !== undefined && translatedSlideHash.includes("-")){
        alreadyHashedCount = translatedSlideHash.split("-")[1];
      }
      var activeIndex = $(".subject-block.active").data("index");
      var currentIndex = $(this).data("index");
      var subtractIndex = alreadyHashedCount === 1 ? 0:1;
      var width = $("body").width();
      //In medium and large screens slideBy is 2
      var j = width < 768 ? 1:2;
      if(j === 1 && activeIndex > currentIndex && alreadyHashedCount !== 1){
        subtractIndex =-1;
      }
      if(activeIndex === currentIndex){
          return;
      }
      else{
        if(activeIndex > currentIndex){
          for(i=currentIndex/j;i<(activeIndex - subtractIndex)/j;i++){
            $("#youtube-video-testimonials").trigger('prev.owl.carousel');
          }
        }
        else{
          for(i=activeIndex/j;i<(currentIndex - subtractIndex)/j;i++){
            $("#youtube-video-testimonials").trigger('next.owl.carousel');
          }
        }
      }
      $(".subject-block").removeClass("active");
      $(this).addClass("active");
    }
    function loadVideoInModel(){
        $this = $(this).find(".video");
        // console.log($this.data("url"));
        Swal.fire({
            width:"42rem",
            html:
            '<div class="embed-responsive embed-responsive-16by9 videoPlayer">'
            +'<iframe width="100%" height="315" src="'+$this.data("url")+'" frameborder="0" allowfullscreen>'
            +'  </iframe>'
            +'</div>',
            showConfirmButton: false,
            showCloseButton: true,
            customClass: "videoPopup",
            onClose : function(){
                $('.videoPlayer').remove();
            }
        });
    }
</script>